-- View: Customers with missing contact information
CREATE OR REPLACE VIEW v_customers_missing_contact AS
SELECT
    client_id,
    customer_name,
    customer_cpf_cnpj,
    lifetime_value,
    num_pedidos_unicos,
    CASE
        WHEN telefone IS NULL THEN 'Missing Phone'
        WHEN endereco_cidade IS NULL THEN 'Missing City'
        WHEN endereco_uf IS NULL THEN 'Missing State'
        ELSE 'Other'
    END as missing_field
FROM public.analytics_gold_customers
WHERE period_type = 'all_time'
  AND (telefone IS NULL OR endereco_cidade IS NULL OR endereco_uf IS NULL)
ORDER BY lifetime_value DESC;

COMMENT ON VIEW v_customers_missing_contact IS 'High-value customers with incomplete contact information';

-- View: Duplicate customer records (should be 0 after unique constraint)
CREATE OR REPLACE VIEW v_duplicate_customer_records AS
SELECT
    client_id,
    customer_name,
    period_type,
    period_start,
    COUNT(*) as duplicate_count
FROM public.analytics_gold_customers
GROUP BY client_id, customer_name, period_type, period_start
HAVING COUNT(*) > 1;

COMMENT ON VIEW v_duplicate_customer_records IS 'Identifies duplicate customer records - should return 0 rows';

-- View: Column consistency check
CREATE OR REPLACE VIEW v_column_consistency_check AS
SELECT
    'Products: total_quantity_sold vs quantidade_total mismatch' as issue,
    COUNT(*) as affected_rows
FROM public.analytics_gold_products
WHERE total_quantity_sold != quantidade_total
UNION ALL
SELECT
    'Products: order_count vs num_pedidos_unicos mismatch' as issue,
    COUNT(*) as affected_rows
FROM public.analytics_gold_products
WHERE order_count != num_pedidos_unicos;

COMMENT ON VIEW v_column_consistency_check IS 'Checks for data inconsistencies between duplicate columns';

