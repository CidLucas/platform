-- Function: Sync duplicate columns in products table
CREATE OR REPLACE FUNCTION sync_product_duplicate_columns()
RETURNS TABLE(
    updated_quantity_count BIGINT,
    updated_orders_count BIGINT
) AS $$
DECLARE
    v_quantity_count BIGINT;
    v_orders_count BIGINT;
BEGIN
    -- Sync total_quantity_sold to quantidade_total
    UPDATE public.analytics_gold_products
    SET total_quantity_sold = quantidade_total
    WHERE total_quantity_sold != quantidade_total;

    GET DIAGNOSTICS v_quantity_count = ROW_COUNT;

    -- Sync order_count to num_pedidos_unicos
    UPDATE public.analytics_gold_products
    SET order_count = num_pedidos_unicos
    WHERE order_count != num_pedidos_unicos;

    GET DIAGNOSTICS v_orders_count = ROW_COUNT;

    RETURN QUERY SELECT v_quantity_count, v_orders_count;
END;
$$ LANGUAGE plpgsql;

COMMENT ON FUNCTION sync_product_duplicate_columns() IS 'Emergency function to sync duplicate column values - should not be needed after ETL fix';

