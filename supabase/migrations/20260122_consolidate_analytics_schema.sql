-- PHASE 6: RLS Policies Verification
-- =====================================================================

-- Verify all analytics tables have RLS enabled
DO $$
DECLARE
    tbl TEXT;
    rls_enabled BOOLEAN;
BEGIN
    FOR tbl IN
        SELECT tablename
        FROM pg_tables
        WHERE schemaname = 'public'
          AND tablename LIKE 'analytics_%'
    LOOP
        SELECT relrowsecurity INTO rls_enabled
        FROM pg_class
        WHERE relname = tbl;

        IF NOT rls_enabled THEN
            RAISE WARNING 'RLS not enabled on table: %', tbl;
        END IF;
    END LOOP;
END;
$$;

